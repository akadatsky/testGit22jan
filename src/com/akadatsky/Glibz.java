package com.akadatsky;

public class Glibz {
    String vesselName;
    int vesselCode;

    public Glibz(String vesselName, int vesselCode) {
        this.vesselName = vesselName;
        this.vesselCode = vesselCode;
    }

    public String getVesselName() {
        return vesselName;
    }

    public int getVesselCode() {
        return vesselCode;
    }
}
